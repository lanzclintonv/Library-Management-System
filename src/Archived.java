    String readFile(String fileName) throws IOException {
    BufferedReader br = new BufferedReader(new FileReader(fileName));
    try {
        StringBuilder sb = new StringBuilder();
        String line = br.readLine();

        while (line != null) {
            sb.append(line);
            sb.append("\n");
            line = br.readLine();
        }
        return sb.toString();
    } finally {
        br.close();
        }
    }
    
    private void RefreshTable()
    {
        for(int i = 0; i < 100; i++)
        {
            for(int j = 0; j < 5; j++)
            {
                tableArchives.setValueAt(books[i][j], i, j);
            }
        }
    }
    
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        File x = new File("archives.txt") ;
        if(x.exists())
        {
            try{
                String text = readFile("archives.txt");
                if(!"".equals(text))
                {
                    String[] words1 = text.split("_\n");
                    rowTrack = words1.length;
                    for(int j = 0; j < 5; j++)
                    {
                        for(int i = 0; i < words1.length; i++)
                        {
                            String[] words2 = words1[i].split("\\\\");
                            books[i][j] = words2[j];
                        }
                    }
                    RefreshTable();
                }
            }
            catch(IOException e)
            {
                JOptionPane.showMessageDialog(null, e, "Error!", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            try
            {
                x.createNewFile();
            }
            catch(IOException e)
            {
                JOptionPane.showMessageDialog(null, e, "Error!", JOptionPane.WARNING_MESSAGE);
            }
            //JOptionPane.showMessageDialog(null, "Path is on: " + x.getPath());
        }
    }//GEN-LAST:event_formWindowOpened

